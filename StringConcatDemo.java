public class StringConcatDemo {

    public static void main(String[] args) {

        // First string variable
        String firstName = "Cris";

        // Second string variable
        String lastName = "Maranan";

        // Concatenate the two strings using the + operator.
        // String concatenation joins two or more strings into one continuous string.
        String fullName = firstName + " " + lastName;

        // Print the result of the string concatenation
        System.out.println("Full Name: " + fullName);
    }
}
