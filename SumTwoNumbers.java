/*
This program calculates the sum of two numbers.
It demonstrates how to use variables to store numeric values,
performs the addition operation, and displays the result.
The purpose is to help learners understand basic arithmetic
and how to output results in Java.
*/

public class SumTwoNumbers {
    public static void main(String[] args) {
        int num1 = 15; // first number
        int num2 = 25; // second number

        int sum = num1 + num2; // calculate sum

        System.out.println("The sum of " + num1 + " and " + num2 + " is: " + sum);
    }
}
