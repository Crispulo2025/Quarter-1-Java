public class MultiplyNumbers {
    public static void main(String[] args) {

        // Declare the first number
        int num1 = 8;

        // Declare the second number
        int num2 = 5;

        // Multiply the two numbers
        int product = num1 * num2;

        // Print the result
        System.out.println("The product is: " + product);
    }
}
