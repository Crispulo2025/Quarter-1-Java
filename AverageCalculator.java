public class AverageCalculator {
    public static void main(String[] args) {
        int num1 = 10;
        int num2 = 20;
        int num3 = 30;

        // calculate average with explicit conversion to double
        double average = (num1 + num2 + num3) / 3.0;

        System.out.println("Average: " + average);
    }
}
