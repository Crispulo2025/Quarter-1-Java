public class MultiLinePrint {

    public static void main(String[] args) {

        // Print the first line of text
        // Each System.out.println() prints text on a new line
        System.out.println("Hello, World!");

        // Print the second line of text
        System.out.println("Welcome to Java programming.");

        // Print the third line of text
        System.out.println("This program demonstrates multiple lines output.");
    }
}
