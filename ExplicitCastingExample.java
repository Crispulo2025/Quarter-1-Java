public class ExplicitCastingExample {
    public static void main(String[] args) {
        double myDouble = 9.78;           // double variable
        int myInt = (int) myDouble;       // explicit type casting to int

        System.out.println("Original double value: " + myDouble);
        System.out.println("Value after explicit casting to int: " + myInt);
    }
}
