// Java program that uses three variables and explains each one

public class InfoDisplay {   // New class name
    public static void main(String[] args) {
        
        int age = 20;               // 'age' stores a person's age in years
        double height = 5.8;        // 'height' stores the person's height in feet
        String name = "Cris";       // 'name' stores the person's name as text

        // Display the values
        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " ft");
    }
}
